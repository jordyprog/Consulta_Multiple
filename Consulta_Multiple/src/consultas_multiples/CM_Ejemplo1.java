    private void mostrar(){
        int t;
        String valor=(String)cboapepat.getSelectedItem();
        try{
            String sql="SELECT c.cod_clie,c.nombre_clie,(c.ape_pat_clie+' '"
            + "+c.ape_mat_clie),v.fec_ven FROM Cliente AS c INNER JOIN Venta AS v ON (c.cod_clie=v.cod_clie) WHERE c.ape_pat_clie='"+valor+"'";
            limpiar();
            stmt=con.createStatement();
            rs=stmt.executeQuery(sql);
            while(rs.next()){
            String []datos01=new String[4];
            datos01[0]=rs.getString(1);
            datos01[1]=rs.getString(2);
            datos01[2]=rs.getString(3);
            datos01[3]=rs.getString(4);
            dtm01.addRow(datos01);
            t=tblclieven.getRowCount();
            txttotal.setText(String.valueOf(t));
            }
//            con.close();
//            stmt.close();
        }
        catch (Exception ex){
            JOptionPane.showMessageDialog(rootPane,ex.getMessage());
        }
    }
    
    private void limpiar(){
        int filas;
        filas=dtm01.getRowCount()-1;
        for(int i=filas;i>=0;i--){
            dtm01.removeRow(i);
        }    
    }
    
